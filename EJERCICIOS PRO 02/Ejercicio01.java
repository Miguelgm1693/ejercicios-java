public class Ejercicio01 {
  public static void main (String[] args) {
    int x;
    int y;
    x = 144;
    y = 999;
  System.out.println("x = " + x);  
  System.out.println("y = " + y);  
  System.out.println("x + y = " + x + y);
  System.out.println("x - y = " + (x - y));
  System.out.println("x * y = " + x * y);
  System.out.println("x / y = " + (double)x / (double)y); 
 }
}

